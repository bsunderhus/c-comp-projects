    void isOk(){
        if(!questionTextArea.getText().trim().isEmpty() 
                && !correctAnswerTextField.getText().trim().isEmpty() 
                && !wrongAnswer1TextField.getText().trim().isEmpty() 
                && !wrongAnswer2TextField.getText().trim().isEmpty() 
                && !wrongAnswer3TextField.getText().trim().isEmpty())
            addQuestionButton.setEnabled(true);
        
        else 
            addQuestionButton.setEnabled(false);
    }
    
