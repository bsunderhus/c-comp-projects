    private void addQuestionButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addQuestionButtonActionPerformed
        
        ArrayList<String> answers = new ArrayList<>();
        answers.add(correctAnswerTextField.getText().trim());
        answers.add(wrongAnswer1TextField.getText().trim());
        answers.add(wrongAnswer2TextField.getText().trim());
        answers.add(wrongAnswer3TextField.getText().trim());
        
        this.mainControl.addQuestion(categoryComboBox.getSelectedItem(),levelComboBox.getSelectedItem(),
                questionTextArea.getText().trim(),answers);
        this.dispose();
    }//GEN-LAST:event_addQuestionButtonActionPerformed

